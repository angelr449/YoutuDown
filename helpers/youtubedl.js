const youtubedl = require('youtube-dl-exec');

/**
 * Fetches metadata information from a YouTube video or playlist
 * WITHOUT downloading any media.
 *
 * Purpose:
 * - Avoids repeated calls to YouTube
 * - Enables local metadata caching
 * - Reduces request aggressiveness (anti-block strategy)
 *
 * @param {string | URL} url
 *   A YouTube video or playlist URL.
 *
 * @param {Object} [flags={}]
 *   Optional yt-dlp flags that override the defaults.
 *
 * @returns {Promise<Object>}
 *   Parsed metadata JSON returned by yt-dlp.
 */
const getInfo = (url, flags = {}) => {
    const isPlaylist = url.includes('playlist') || url.includes('list=');

    return youtubedl(url, {
        /* Core behavior */
        dumpSingleJson: true,   // Return metadata as JSON
        skipDownload: true,    // Do NOT download media

        /* Anti-block strategy */
        sleepInterval: 3,        // Minimum seconds between requests
        maxSleepInterval: 8,     // Randomized upper delay bound
        retries: 2,              // Low retry count to reduce fingerprinting
        fragmentRetries: 2,      // Same logic for fragment retries
        socketTimeout: 30,       // Network stability
        concurrentFragments: 1,  // Disable parallel fragment downloads

        /* Realistic browser fingerprint */
        userAgent:
            'Mozilla/5.0 (X11; Linux x86_64) AppleWebKit/537.36 ' +
            '(KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36',

        /* Optimize playlist handling */
        ...(isPlaylist ? { flatPlaylist: true } : {}),

        ...flags
    });
};

/**
 * Fetches metadata optimized for streaming or downloading workflows.
 *
 * Differences from getInfo():
 * - Allows controlling whether playlist entries should be flattened
 * - Supports use cases where real URLs are required (downloads)
 *
 * @param {string | URL} url
 *   A YouTube video or playlist URL.
 *
 * @param {Object} [options={}]
 * @param {boolean} [options.forDownload=false]
 *   If true, returns full playlist URLs instead of flat IDs.
 *
 * @param {Object} [options.flags]
 *   Additional yt-dlp flags.
 *
 * @returns {Promise<Object>}
 *   Parsed metadata JSON returned by yt-dlp.
 */
const getInfoForStream = (url, { forDownload = false, ...flags } = {}) => {
    const isPlaylist = url.includes('list=');

    return youtubedl(url, {
        dumpSingleJson: true,
        skipDownload: true,

        /* Anti-block strategy */
        sleepInterval: 3,
        maxSleepInterval: 8,
        retries: 2,
        fragmentRetries: 2,
        socketTimeout: 30,
        concurrentFragments: 1,

        /* Browser fingerprint */
        userAgent:
            'Mozilla/5.0 (X11; Linux x86_64) AppleWebKit/537.36 ' +
            '(KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36',

        /**
         * flatPlaylist behavior:
         * - true  → Only video IDs (fast, ideal for UI previews)
         * - false → Full video URLs (required for downloads)
         */
        ...(isPlaylist && !forDownload ? { flatPlaylist: true } : {}),

        ...flags
    });
};

/**
 * Downloads a YouTube video using a previously saved metadata JSON file.
 *
 * Purpose:
 * - Separates metadata extraction from downloading
 * - Reduces repeated scraping
 * - Minimizes YouTube requests
 *
 * @param {string} infoFile
 *   Path to the JSON file generated by getInfo().
 *
 * @param {Object} [flags={}]
 *   Optional yt-dlp flags (format, output template, etc.).
 *
 * @returns {Promise<void>}
 */
const fromInfo = (infoFile, flags = {}) => {
    return youtubedl.exec('', {
        loadInfoJson: infoFile, // Load metadata directly from disk

        /* Same anti-block profile used during info extraction */
        sleepInterval: 3,
        maxSleepInterval: 8,
        retries: 2,
        fragmentRetries: 2,
        concurrentFragments: 1,

        ...flags
    });
};

module.exports = {
    getInfo,
    getInfoForStream,
    fromInfo
};
